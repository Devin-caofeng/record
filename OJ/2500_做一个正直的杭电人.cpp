#include <iostream>
using namespace std;


int main() {

    int cnt;
    cin >> cnt;

    while (cnt--) {
        int n;
        cin >> n;

        for (int i = 0; i < 3 * n; ++i) {
            for (int j = 0; j < n; ++j)
                printf("HDU");
            printf("\n");
        }
    }

    return 0;
}
